package TaskTwo.POO.XeY;
// Crie um algoritmo que leia dois valores (x e y) e escreva na tela qual o maior

public class XeY {
    public static void main(String[] args) {

        int x = 30;
        int y = 40;

        if (x > y){
            System.out.println("X é maior que Y");
        } else if (y > x){
            System.out.println("Y é maior que X");
        }
    }
}
